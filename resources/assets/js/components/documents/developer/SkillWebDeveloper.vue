              <li>Kiến thức về database SQL: MS SQL Server, MySQL, … Gần đây một số database NoSQL đang khá thịnh hành: Neo4j, MongoDB, …</li>
              <li>Kiến thức về web nói chung, cách viết Web Service, cách đăng nhập và phân quyền .</li>
              <li>Kiến thức về 1 số CMS: WordPress, Joomla, Umbraco, ….</li>
            </ul>
            <p>
